export const DETECTED_OS = (() => {
  const ua = typeof window !== 'undefined' ? window.navigator.userAgent.toLowerCase() : '';
  if (ua.includes('win')) return 'windows';
  if (ua.includes('mac')) return 'macos';
  return 'linux';
})();
